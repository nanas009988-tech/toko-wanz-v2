const paymentMethods = [
    "DANA",
    "OVO",
    "GOPAY",
    "QRIS"
];

let selectedGame = "";
let selectedNominal = null;
let selectedPayment = "";

document.addEventListener("DOMContentLoaded", () => {
    loadGameData();
    renderNominals();
    renderPayments();
    initializeEvents();
    updateSummary();
});

function loadGameData() {

    selectedGame =
        localStorage.getItem("selectedGame") ||
        "Mobile Legends";

    const gameName =
        document.getElementById("game-name");

    const gameImage =
        document.getElementById("game-image");

    if (gameName) {
        gameName.textContent = selectedGame;
    }

    if (gameImage) {
        gameImage.src =
            gameAssets[selectedGame];

        gameImage.alt =
            selectedGame;
    }
   const zoneWrapper =
    document.getElementById("zone-wrapper");

if (zoneWrapper) {
    zoneWrapper.style.display =
        selectedGame === "Mobile Legends" ||
        selectedGame === "Magic Chess"
            ? "block"
            : "none";
}
}

function renderNominals() {

    const nominalGrid =
        document.getElementById("nominal-grid");

    if (!nominalGrid) return;

    nominalGrid.innerHTML = "";

    let products =
    productData[selectedGame] || [];

if (selectedGame === "FC Mobile") {
    products = [...products].sort((a, b) => {

        const aSilver =
            a.name.includes("Silver");

        const bSilver =
            b.name.includes("Silver");

        if (aSilver && !bSilver) return -1;
        if (!aSilver && bSilver) return 1;

        return 0;
    });
}
        let membershipTitleAdded = false;
        let diamondTitleAdded = false;

    products.forEach((item) => {
      
    if (item.title) {

    const title =
        document.createElement("h3");

    title.textContent =
        item.name;

    title.className =
        "category-title";

    nominalGrid.appendChild(title);

    return;
}

    const isMembership =
    item.name.includes("Silver") ||
    item.name === "Membership Mingguan" ||
    item.name === "BP Card" ||
    item.name === "Member Bulanan";

    if (isMembership && !membershipTitleAdded) {

    const title = document.createElement("h3");

    title.textContent =
        selectedGame === "FC Mobile"
            ? "Silver"
            : "Membership";

    title.className = "category-title";

    nominalGrid.appendChild(title);

    membershipTitleAdded = true;
}

if (!isMembership && !diamondTitleAdded) {

    const title = document.createElement("h3");

    title.textContent =
        selectedGame === "FC Mobile"
            ? "FC Points"
            : "Top Up Instan";

    title.className = "category-title";

    nominalGrid.appendChild(title);

    diamondTitleAdded = true;
}

    if (!isMembership && !diamondTitleAdded) {

    if (selectedGame !== "PUBG Mobile") {

        const title =
            document.createElement("h3");

        title.textContent =
            "Top Up Instan";

        title.className =
            "category-title";

        nominalGrid.appendChild(title);
    }

    diamondTitleAdded = true;
}

    const card =
        document.createElement("div");

        card.className =
            "nominal-card";

        card.innerHTML = `
            ${item.bestSeller ? `
                <span class="best-seller">
                    Best Seller
                </span>
            ` : ""}

            <span class="nominal-name">
                ${item.name}
            </span>

            <strong class="nominal-price">
                ${formatRupiah(item.price)}
            </strong>
        `;

        card.addEventListener("click", () => {

            document
                .querySelectorAll(".nominal-card")
                .forEach((element) => {
                    element.classList.remove("active");
                });

            card.classList.add("active");

            selectedNominal = item;

            updateSummary();

document
  .getElementById("step-payment")
  ?.scrollIntoView({
    behavior: "smooth",
    block: "start"
  });
        });

        nominalGrid.appendChild(card);
    });
}

function renderPayments() {

    const paymentGrid =
        document.getElementById("payment-grid");

    if (!paymentGrid) return;

    paymentGrid.innerHTML = "";

    paymentMethods.forEach((method) => {

        const card =
            document.createElement("div");

        card.className =
            "payment-card";

        let logo = "";

if (method === "DANA") {
    logo = "assets/payment/dana.png";
} else if (method === "OVO") {
    logo = "assets/payment/ovo.png";
} else if (method === "GOPAY") {
    logo = "assets/payment/gopay.png";
}

card.innerHTML = `
    <img src="${logo}" class="payment-logo">
    <span>${method}</span>
`;

        card.addEventListener("click", () => {

            document
                .querySelectorAll(".payment-card")
                .forEach((element) => {
                    element.classList.remove("active");
                });

            card.classList.add("active");

            selectedPayment =
                method;

            updateSummary();
            
           document
.getElementById("summary-title")
    ?.scrollIntoView({
        behavior: "smooth",
        block: "start"
    });
        });

        paymentGrid.appendChild(card);
    });
}

function initializeEvents() {

    const form =
        document.getElementById("topup-form");

    const userId =
        document.getElementById("user-id");

    const serverId =
        document.getElementById("server-id");

    if (userId) {
        userId.addEventListener(
            "input",
            updateSummary
        );
    }

    if (serverId) {
        serverId.addEventListener(
            "input",
            updateSummary
        );
    }

    if (form) {
        form.addEventListener(
            "submit",
            handleCheckout
        );
    }
}

function updateSummary() {

    const userId =
        document.getElementById("user-id")?.value || "";

    const serverId =
        document.getElementById("server-id")?.value || "";

    document.getElementById("summary-game").textContent =
        selectedGame;

    document.getElementById("summary-user").textContent =
        userId
            ? serverId
                ? `${userId} (${serverId})`
                : userId
            : "-";

    document.getElementById("summary-nominal").textContent =
        selectedNominal
            ? selectedNominal.name
            : "-";

    document.getElementById("summary-payment").textContent =
        selectedPayment || "-";

    document.getElementById("summary-total").textContent =
        selectedNominal
            ? formatRupiah(selectedNominal.price)
            : "Rp0";
}

function handleCheckout(event) {

    event.preventDefault();

    const userId =
        document.getElementById("user-id")
            .value
            .trim();

    const serverId =
        document.getElementById("server-id")
            .value
            .trim();

    if (!userId) {
        showToast("Masukkan User ID terlebih dahulu");
        return;
    }

    if (!selectedNominal) {
        showToast("Pilih nominal terlebih dahulu");
        return;
    }

    if (!selectedPayment) {
        showToast("Pilih metode pembayaran");
        return;
    }

    const message = `Halo Admin TopUpCode

🎮 Game : ${selectedGame}
🆔 User ID : ${userId}
🌐 Server ID : ${serverId || "-"}
💎 Nominal : ${selectedNominal.name}
💰 Harga : ${formatRupiah(selectedNominal.price)}
💳 Pembayaran : ${selectedPayment}

Mohon diproses, terima kasih.`;

    window.open(
    `https://wa.me/6288213456031?text=${encodeURIComponent(message)}`,
    "_blank"
);
}

function formatRupiah(number) {
    return new Intl.NumberFormat(
        "id-ID",
        {
            style: "currency",
            currency: "IDR",
            maximumFractionDigits: 0
        }
    ).format(number);
}

function showToast(message) {

    const container =
        document.getElementById("toast-container");

    if (!container) return;

    const toast =
        document.createElement("div");

    toast.className = "toast";
    toast.textContent = message;

    container.appendChild(toast);

    setTimeout(() => {

        toast.style.opacity = "0";

        setTimeout(() => {
            toast.remove();
        }, 300);

    }, 2500);
}
